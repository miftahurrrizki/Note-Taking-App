    private void btn_signupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_signupActionPerformed
        String newUserName = input_username_signup.getText();
        String newPassword = input_password1_signup.getText();
        
        User usr = new User(newUserName, newPassword);       
        DatabaseManager oh = new DatabaseManager();

        // Periksa apakah username atau password kosong
        if (usr.getUsername().trim().isEmpty() || usr.getPassword().trim().isEmpty()) {
            JOptionPane.showMessageDialog(btn_signup, "Username dan Password Tidak Boleh Kosong!");
            return; // Keluar dari metode jika username atau password kosong
        }
        // Panggil metode saveNewUser dari objek DatabaseManager
        if (oh.userSignup(usr.getUsername(), usr.getPassword())) {
            // Jika metode mengembalikan true, tampilkan pesan sukses dan buka Home
            dispose();
            Login ah = new Login();
            ah.setTitle("Selamat Datang");
            ah.setVisible(true);
            ah.setLocationRelativeTo(null);
            JOptionPane.showMessageDialog(btn_signup, "Anda Berhasil SignUp, Silahkan Login");
        } else {
            // Jika metode mengembalikan false, tampilkan pesan kesalahan spesifik
            JOptionPane.showMessageDialog(btn_signup, "Username Sudah Digunakan, Pilih Username Lain! üôè");
        }
    }//GEN-LAST:event_btn_signupActionPerformed

