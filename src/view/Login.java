        // TODO add your handling code here:
        String userName = input_username_login.getText();
        String password = input_password_login.getText();
        User usr = new User(userName,password);
        DatabaseManager oh = new DatabaseManager();
        currentUser = usr.getUsername();                

        if (oh.userLogin(usr.getUsername(), usr.getPassword())) {
            dispose();
            Home ah = new Home();
            ah.setTitle("Home");
            ah.setLocationRelativeTo(null);
            ah.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(btn_login, "Username & Password Tidak Sesuai ðŸ˜­");
        }        
